									<a href="" data-toggle="collapse" data-target="#demo4">switching to a green energy supplier</a><br>
									<div id="demo4" class="collapse">
										In UK grid, the electricity emission factor is 0.2556 kg CO2e/kWh. In Scotland, the factor number should be lower, because 51.7% of electricity 
										is generated from renewables, compared to 25.6% for the rest of UK, but detailed emission factor not published. If purchased from 100% renewable
										supplier, the emission factor is 0 kg CO2e/kWh (theoretical, subject to the number provided by energy supplier). Electricity emission in scope 2 
										reduced to 0, heating & cooling emission unchanged. The percentage of emission reduction (kWh * 0.2556 kg CO2e/kWh) depends on a company’s specific 
										electricity consumption. Meanwhile, by switching to a green supplier, the energy cost will generally remains the same.
										Beyond changes that simply drive down emissions but impact insignificantly on company costs, there are also more changes that businesses can make 
										to reduce emission and save money simultaneously. Furthermore, companies can choose to sign to connected behaviour changes (like Solar PV Panel 
										installation and LED lights replacement) to grab synergy of reducing emissions as well as saving costs.

