										There are various forms of grants available in the UK to incentivise the purchase of electric vehicles. Capital allowances can be claimed on low emission cars 
										that reduces the amount of tax businesses are required to pay. On a car costing around £40,000 this could amount to a tax relief of £7,600 in the first year. 
