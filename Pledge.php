									<a href="" data-toggle="collapse" data-target="#demo3">Reducing waste to landfill</a><br>
									<div id="demo3" class="collapse">
										Recycling also means less waste going to landfill, which means less methane generated. Methane, a greenhouse gas 23 times more potent than carbon dioxide, 
										is produced when organic materials—such as paper, food and grass—decompose under the anaerobic (without oxygen) conditions present in landfills. In fact,
										landfills are currently the leading source of methane. Recycling food scraps keeps organics out of the landfill and prevents methane generation. 
 
